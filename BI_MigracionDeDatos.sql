------------------- Tabla intermedia datos automoviles  -------------------

SELECT 

  ac.SUCURSAL_CODIGO, ac.anio, ac.mes, cantidad_comprada, 
  promedio_compra, cantidad_vendida, promedio_venta, (precio_vendido - precio_compra) ganancias
INTO #automoviles
FROM 
  (
  -- AUTOMOVILES x MES x SUCURSAL: CANTIDAD COMPRADA Y PRECIO TOTAL COMPRADO
  SELECT SUCURSAL_CODIGO, MONTH(COMPRA_FECHA) mes, YEAR(COMPRA_FECHA) anio, 
      COUNT(*) cantidad_comprada, SUM(c.PRECIO_TOTAL) precio_compra,AVG(PRECIO_TOTAL) promedio_compra
  FROM UNIX.COMPRA c
  WHERE TIPO_COMPRA = 'AM'
  GROUP BY SUCURSAL_CODIGO, MONTH(COMPRA_FECHA),YEAR(COMPRA_FECHA)
  ) ac
JOIN 
  (
  -- AUTOMOVILES x MES x SUCURSAL: CANTIDAD VENDIDA  Y PRECIO TOTAL VENDIDO
  SELECT SUCURSAL_CODIGO, MONTH(FACTURA_FECHA) mes, YEAR(FACTURA_FECHA) anio,
    COUNT(*) cantidad_vendida, SUM(PRECIO_TOTAL) precio_vendido, AVG(PRECIO_TOTAL) promedio_venta
  FROM UNIX.Factura f
  WHERE TIPO_PRODUCTO = 'AM'
  GROUP BY SUCURSAL_CODIGO, MONTH(FACTURA_FECHA),YEAR(FACTURA_FECHA)
  ) av
ON (
    ac.SUCURSAL_CODIGO = av.SUCURSAL_CODIGO AND
    ac.anio = av.anio AND
    ac.mes = av.mes
  )


------------------- Tabla intermedia datos autopartes  -------------------

SELECT ac.SUCURSAL_CODIGO, ac.anio, ac.mes, (precio_vendido - precio_comprado) GANANCIAS
INTO #autopartes
FROM 
  (
  -- AUTOPARTES x MES x SUCURSAL: PRECIO TOTAL COMPRADO
  SELECT SUCURSAL_CODIGO, MONTH(COMPRA_FECHA) mes, YEAR(COMPRA_FECHA) anio,
      SUM(PRECIO_TOTAL) precio_comprado
  FROM UNIX.COMPRA c
  WHERE TIPO_COMPRA = 'AP'
  GROUP BY SUCURSAL_CODIGO, MONTH(COMPRA_FECHA),YEAR(COMPRA_FECHA)
) ac
JOIN 
  (
  -- AUTOPARTES x MES x SUCURSAL: PRECIO TOTAL VENDIDO
  SELECT SUCURSAL_CODIGO, MONTH(FACTURA_FECHA) mes, YEAR(FACTURA_FECHA) anio,
      SUM(PRECIO_TOTAL) precio_vendido
  FROM UNIX.Factura f
  WHERE TIPO_PRODUCTO = 'AP'
  GROUP BY SUCURSAL_CODIGO, MONTH(FACTURA_FECHA),YEAR(FACTURA_FECHA)
  ) av 
ON
  (
  ac.SUCURSAL_CODIGO = av.SUCURSAL_CODIGO AND
  ac.anio = av.anio AND
  ac.mes = av.mes
  )
ORDER BY SUCURSAL_CODIGO, anio, mes



SELECT ac.SUCURSAL_CODIGO,ac.AUTO_PARTE_CODIGO, ac.anio, ac.mes, cantidad_total_comprada ,compra_total 
INTO #autopartescompra
FROM 
  (
  -- AUTOPARTES x MES x SUCURSAL: PRECIO TOTAL COMPRADO
  SELECT SUCURSAL_CODIGO,AUTO_PARTE_CODIGO, MONTH(COMPRA_FECHA) mes, YEAR(COMPRA_FECHA) anio,
      SUM(ca.COMPRA_CANT) cantidad_total_comprada ,SUM(PRECIO_TOTAL) compra_total
  FROM UNIX.COMPRA c
  JOIN UNIX.CompraPorAutoparte ca ON(c.COMPRA_NRO=ca.COMPRA_NRO)
  WHERE TIPO_COMPRA = 'AP'
  GROUP BY SUCURSAL_CODIGO, AUTO_PARTE_CODIGO, MONTH(COMPRA_FECHA),YEAR(COMPRA_FECHA)
) ac

SELECT ac.SUCURSAL_CODIGO,ac.AUTO_PARTE_CODIGO, ac.anio, ac.mes, cantidad_total_vendida ,venta_total 
INTO #autopartesventa
FROM 
  (
  -- AUTOPARTES x MES x SUCURSAL: PRECIO TOTAL VENDIDO
  SELECT SUCURSAL_CODIGO,AUTO_PARTE_CODIGO, MONTH(FACTURA_FECHA) mes, YEAR(FACTURA_FECHA) anio,
      SUM(ia.FACTURA_CANT) cantidad_total_vendida ,SUM(PRECIO_TOTAL) venta_total
  FROM UNIX.Factura f
  JOIN UNIX.ItemAutoparte ia ON(f.FACTURA_NRO=ia.FACTURA_NRO)
  WHERE TIPO_COMPRA = 'AP'
  GROUP BY SUCURSAL_CODIGO, AUTO_PARTE_CODIGO, MONTH(COMPRA_FECHA),YEAR(COMPRA_FECHA)
) ac

